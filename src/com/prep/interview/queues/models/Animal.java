package com.prep.interview.queues.models;

public class Animal {

    String name;
    int order;

    public void setOrder(int order) {
        this.order = order;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public int getOrder() {
        return order;
    }
}
